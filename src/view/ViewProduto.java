    private void btnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelActionPerformed
        /*
         Metodo para Botão Deletar Produtos
         */
        int linha = tblProd.getSelectedRow();
        int codProd = (int) tblProd.getValueAt(linha, 0);
        if (controllerProdutos.excluirProdutosController(codProd)) {
            JOptionPane.showMessageDialog(this, "Produto Excluído com Sucesso!", " WA-WA-WIIUAH", JOptionPane.WARNING_MESSAGE);
            this.carregarProdutos();
            this.limparCampos();
            this.habilitarCampos(false);
        } else {
            JOptionPane.showMessageDialog(this, " Erro ao Excluir Produto!", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDelActionPerformed

