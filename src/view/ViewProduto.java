    private void carregarProdutos() {
        listaModelProduto = controllerProdutos.retornarListaProdutoController();
        DefaultTableModel modelo = (DefaultTableModel) tblProd.getModel();
        modelo.setNumRows(0);
        int cont = listaModelProduto.size();

        for (int i = 0; i < cont; i++) {

            modelo.addRow(new Object[]{
                listaModelProduto.get(i).getProdId(),
                listaModelProduto.get(i).getProdNome(),
                listaModelProduto.get(i).getProdEstoque(),
                listaModelProduto.get(i).getProdValor()
            });

        }
    }

