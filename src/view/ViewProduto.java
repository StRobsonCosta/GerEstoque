    private void btnAltActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAltActionPerformed
        /*
         Metodo para Botão Alterar dados
         */
        salvarAlterar = "alterar";
        habilitarCampos(true);
        int linha = this.tblProd.getSelectedRow();

        try {
            int codProd = (int) this.tblProd.getValueAt(linha, 0);
            modelProdutos = controllerProdutos.retornarProdutoController(codProd);
            this.txtCod.setText(String.valueOf(modelProdutos.getProdId()));
            this.txtNome.setText(modelProdutos.getProdNome());
            this.txtEstoque.setText(String.valueOf(modelProdutos.getProdEstoque()));
            this.txtValor.setText(String.valueOf(modelProdutos.getProdValor()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Codigo Invalido ou Nenhum Registro Selecionado", "AVISO", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_btnAltActionPerformed

    private void btnPesqActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesqActionPerformed
        /*
         * Metodo para Botão Pesquisar Produtos
         * Refatorar com sistema de alto preenchimento como no ProjNilu
         */
        DefaultTableModel modelo = (DefaultTableModel) this.tblProd.getModel();
        final TableRowSorter<TableModel> classificador = new TableRowSorter<>(modelo);
        this.tblProd.setRowSorter(classificador);
        String texto = this.txtPesq.getText();
        classificador.setRowFilter(RowFilter.regexFilter(texto, 1));
    }//GEN-LAST:event_btnPesqActionPerformed

